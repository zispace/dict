function sanitizePinyin(str) {
    const str2 = str
        .toLowerCase()
        .trim()
        .replace(/[0-9]+$/g, '')
    if (str2 == "ei") {
        return "ê";
    }
    return str2.normalize('NFKD')
        .replace(/[\u0300-\u036F]/g, '')  // 移除声调符号
        .replace(/v/g, 'ü')       // 把 v 换成 ü
}

function sanitizePinyin2(str) {
    return str
        .toLowerCase().trim()
        .replace(/v/g, 'ü')
        .replace(/•/g, '·')
}

function isNumeric(str) {
    if (typeof str !== 'string') return false;
    return !isNaN(str) && !isNaN(parseFloat(str));
}

function pad(num, totalLength) {
    return num.toString().padStart(totalLength, "0");
}

function searchImage() {
    const searchInput = document.getElementById("searchInput").value;
    let pageNumber = 0;
    if (isNumeric(searchInput)) {
        pageNumber = parseInt(searchInput) + pinyinIndex - 1;
        if (pageNumber < pinyinIndex || pageNumber > pinyinMaxIndex) {
            pageNumber = null;
        }
    } else {
        const pinyin2 = sanitizePinyin2(searchInput);
        const result = PINYIN_PAGES.find(item => item.name.toLowerCase() === pinyin2 || item.alias.toLowerCase() === pinyin2);
        if (result) {
            pageNumber = result.page + pinyinIndex - 1;
        } else {
            const pinyin = sanitizePinyin(searchInput);
            pageNumber = pinyin === "" ? pinyinIndex : getPageNumberByName(pinyin);
        }
    }

    if (pageNumber !== null && pageNumber >= 1 && pageNumber <= totalImages) {
        document.getElementById("search-result").innerHTML = "";
        currentImageIndex = pageNumber;
        showImage();
    } else {
        document.getElementById("search-result").innerHTML = `检索的拼音（及声调）、正文页码（1～1766）或关键词无效，请重新输入!`;
        // alert("请输入一个有效的拼音或正文页码!");
    }
}

function getPageNumberByName(name) {
    const result = BOOKMARKS.find(item => item.name.toLowerCase() === name);
    if (result) {
        return result.page;
    } else {
        const result2 = ALIAS.find(item => item.name.toLowerCase() === name);
        return result2 ? result2.page : null;
    }
}

function showImage() {
    const isExtra = currentImageIndex < ignoreMinPage || currentImageIndex >= ignoreMaxPage;
    const imageDir = isExtra ? "images-extra" : "images";
    const imageSuffix = isExtra ? "webp" : "png";
    const imageName = pad(currentImageIndex, 4);
    document.getElementById("main-image").src = `${imageDir}/${imageName}.${imageSuffix}`;
}

function changeImage(offset) {
    currentImageIndex += offset;
    if (currentImageIndex < 1) {
        currentImageIndex = 1;
    } else if (currentImageIndex > totalImages) {
        currentImageIndex = totalImages;
    }
    showImage();
}

